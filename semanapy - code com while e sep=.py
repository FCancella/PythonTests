contagem = 3 #marca o inicio da contagem

while contagem > 0: #enquanto contagem for <0
    print(contagem, '...', sep="") #printe o numero de contagem, com "...", separado por ""(nada)
    contagem -=1                    # Nesse caso, sep="", significa separar os printes que serao feitos com uma string ""(vaiza) no meio
print ('BOOOOM!')